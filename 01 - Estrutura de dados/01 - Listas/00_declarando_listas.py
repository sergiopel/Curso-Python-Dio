# lista com 3 frutas
frutas = ["laranja", "maca", "uva"]
print(frutas)

# lista vazia
frutas = []
print(frutas)

# cada letra da palavra python vira um valor dentro da lista
letras = list('python')
print(letras)

# cria lista com os números 0 a 9
numeros = list(range(10))
print(numeros)

# lista com elementos de diferentes tipos
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
