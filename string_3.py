nome = "Pedro Álvares Cabral"

# pega a primeira posição
print(nome[0])

# a partir da primeira posição, pela 9 posições
print(nome[:9])

# a partir da posição 10, pega o restante
print(nome[10:])

# a partir da posição 10, pega 6 posições, ou seja, pega
# da 11 a 16
print(nome[10:16])

# a partir da posição 10, pega 6 posições, ou seja, pega
# da 11 a 16, mas a cada 2 posições
print(nome[10:16:2])

# pega a string inteira
print(nome[:])

# pega a string inteira, de trás para frente (bem legal esse)
print(nome[::-1])

# pegar a última letra
print(nome[-1])

# pegar a antepenúltima letra
print(nome[-2])